#!/usr/bin/env python3
"""
This is a module to perform additional maths operation.
"""


def add(a: float, b: float) -> float:
    """
    This is a function to add two numbers (float)

    Args:
        a (float) - Floating number
        b (float) - Floating number

    Return:
        (float) A floating number which is the sum of {a} and {b}
    """
    return (a + b)
