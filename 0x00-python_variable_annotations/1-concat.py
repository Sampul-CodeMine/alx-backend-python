#!/usr/bin/env python3
"""
This is a module to perform string concatenation operation
"""


def concat(str1: str, str2: str) -> str:
    """
    This is a function to concatenates two strings

    Args:
        str1 (str) - First string argument
        str2 (str) - second string argument

    Return:
        (str) a concatenation of {str1} and {str2}
    """
    return f"{str1}{str2}"
